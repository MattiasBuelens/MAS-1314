package mas.daan;

public class InvalidRouteException extends Exception {
	
	
		  public InvalidRouteException() { super(); }
		  
		  public InvalidRouteException(String message) { super(message); }
		  
		  public InvalidRouteException(String message, Throwable cause) { super(message, cause); }
		  
		  public InvalidRouteException(Throwable cause) { super(cause); }
		

}
